				<p>prosesor adalah salah satu komponen yang paling utama dari rangkaian komputer, tanpa alat satu ini kompi tidak akan jalan alias tidak akan berguna karena prosesor adalah otak komputer Umumnya pengertian processor ataupun sering disebut otak komputer, secara jelasnya prosesor adalah sebuah IC yang mengontrol keseluruhan jalannya sebuah sistem komputer dan digunakan sebagai pusat atau otak dengan fungsi melakukan perhitungan dan menjalankan tugas. 
				Pada umumnya fungsi processor (prosesor) adalah hanya untuk memproses data yang diterima dari masukkan atau di input, kemudian akan menghasilkan pengeluaran berupa output. 
				prosesor tidak dapat bekerja sendiri namun membutuhkkan dukungan maupun terus berhubungan dengan komponen lain terutama hardisk dan RAM. Dalam memproses sebuah data dapat dilakukan dengan waktu proses cepat atau lambat tergantung kecepatan prosesor tersebut. </p>
			
