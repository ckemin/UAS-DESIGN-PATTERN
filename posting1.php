				<p>Mouse komputer merupakan perangkat keras yang termasuk dalam golongan perangkat input (masukan). Fungsi mouse komputer adalah mengatur pergerakan kursor secara cepat, selain itu juga untuk memberikan suatu perintah dengan hanya menekan tombol pada mouse komputer. Di dalam perangkat mouse ini, terdapat sebuah bola kecil yang akan menangkap pergerakan mouse dan mentransfer sinyal listrik ke perangkat pemroses (CPU). Dengan demikian, mouse komputer dapat secara cepat melakukan kinerjanya sebagai perangkat masukan.
				Mouse diciptakan dengan tujuan untuk membantu keyboard dalam melakukan pengoperasian komputer serta penggunaannya yang fleksibel dan mudah sehingga membantu pekerjaan agar lebih cepat selesai. Meskipun laptop saat ini fungsi mouse telah digantikan oleh touchpad yang terdapat pada laptop, namun mouse masih menjadi favorit bagi pengguna untuk menggerakan kursornya.</p>
			
			
											
