			<ul>
			<li><h3>Pengertian Monitor</h3></li>
			</ul>
				<p>Monitor adalah perangkat keras yang digunakan sebagai alat output data secara grafis pada sebuah CPU, monitor juga kerap disebut sebagai layar tampilan komputer. Monitor merupakan salah satu perangkat keras (Hardware) yang digunakan sebagai penampilan output video dari pada sebuah CPU, dan kegunaannya tersebut tidak dapat dipisahkan dalam pemakaian suatu komputer, sehingga dikarenakan monitor itu sebagai penampilan gambar maka tentunya komputer sangat sulit digunakan dan bahkan sama sekali tidak dapat digunakan tanpa menggunakan monitor. Monitor disebut juga dengan VDU (Visual Display Unit).
				Monitor berfungsi sebagai Output dari memori komputer atau central processing unit berupa biner. Ini harus diubah menjadi bahasa manusia dan ditampilkan kemonitor sehingga bisa dibaca oleh pengguna.
				Semua monitor memiliki jenis resolusi yang digunakan untuk menampilkan gambar. Ukuran inci LCD memberitahu apa jenis resolusi yang tersedia. Sebuah layar monitor 17-inci dapat memiliki resolusi 1024×768, sedangkan layar 20-inci akan memiliki 1600×1200. Jumlah dalam inci adalah ukuran layar monitor diagonal, sementara resolusi adalah lebar pixel dengan tinggi pixel. Meskipun laptop memiliki built-in monitor, beberapa laptop tersedia dengan port S-Video, yang memungkinkan kabel S-Video untuk plug ke televisi tertentu. Ketika televisi berubah ke input yang tepat, akan bertindak sebagai cloning.</p>
			
			
				
