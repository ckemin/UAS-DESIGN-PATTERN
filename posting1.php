				<p>Keyboard adalah sebuah perangkat keras (hardware) pada komputer yang berfungsi sebagai alat untuk input data yang berupa huruf, angka dan simbol. Atau definisi keyboard yaitu suatu perangkat keras pada komputer yang berbentuk papan dan memiliki berbagai macam tombol yang dimana tombol tersebut fungsinya berbeda tergantung pada penekanannya yang bisa menghasilkan proses yang sesuai dengan keinginan penggunanya. 
				Keyboard juga dapat dihubungkan pada CPU komputer lewat kabel USB maupun dengan perangkat lain seperti perangkat wireless atau Bluetooth yang juga menggunakan port USB. Berbeda dengan keyboard yang menggunakan kabel sebagai penghubungnya, wireless keyboard atau keyboard tanpa kabel dapat menyala hanya dengan menggunakan baterai.</p>
			
			
				
